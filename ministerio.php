			<p>A aposta no capital humano na boa governação e na administração da justiça é fundamental para a coesão social e a criação de condições imprescindíveis para o fomento do processo produtivo e do crescimento da economia do País. Para além de assegurar a confiança e a aproximação entre os governantes e os governados, a boa governação reforça a credibilidade externa de São Tomé e Príncipe, criando melhores condições para o reforço da cooperação tendente ao crescimento da economia. Cientes destes factos, e tendo ainda em linha de conta o estado actual da nossa justiça, a sua degradação, a qualidade dos serviços prestados, a falta de confiança dos cidadãos nas instituições da justiça, o XVI Governo Constitucional considera fundamental levar a cabo um conjunto de reformas e acções no domínio da Justiça.</p>	  
