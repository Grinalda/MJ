			<p><b>Eficácia</b> – Assegurar e garantir a justiça célere, acessível, independente, livre de qualquer pressão externa e ancorada nos princípios da separação de poderes.</p>
			<p><b>Eficiência</b> – Promover a melhoria contínua do nível da quantidade e qualidade dos recursos humanos para assegura a justiça para todos, aumentando progressivamente a produtividade do pessoal.</p>
			<p><b>Integridade</b> - Compromisso dos servidores da justiça no sentido de trabalharem de forma honesta e dedicada para o cumprimento da missão da Justiça, na excelência da gestão visando o cumprimento integral da missão, serviços da Justiça geridos na base dos princípios de boa gestão e centrados na protecção dos interesses do Cidadão.</p>
			<p><b>Inovação</b> – Promover a inovação organizacional, introduzindo e incentivando uma cultura de rigor e imparcialidade, de respeito pela legalidade e transparência, de cumprimento dos deveres legais, da ética e deontologia profissionais, de mudança de atitudes, práticas, processos e procedimentos administrativos e assegurar a orientação pelo sentido do dever e serviço público.</p>
