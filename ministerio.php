			<p><i>“A Justiça é a pedra basilar de qualquer democracia e a sua confiabilidade é uma das condições prévias ao crescimento da criação de riqueza numa economia liberal”. A estratégia que será implementada terá como objectivo, através de reformas institucionais e legislativas profundas e programas ambiciosos de investimento, assentar e consolidar as bases de uma justiça protectora das pessoas e dos bens, de uma forma imparcial e diligente, com enfoque na formação de magistrados e funcionários judiciais e nas suas condições de emprego, incluindo a sua remuneração, garantias legais de independência, no reforço das instituições de controlo e inspecção, e na construção e aprovisionamento em equipamentos dos tribunais”. </i></p>
